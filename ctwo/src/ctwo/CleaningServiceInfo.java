    public ArrayList<UserT>userList()
    {
      ArrayList<UserT>userList = new ArrayList<>();

         try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=CMS", "sa", "62625522");
         
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement
                    .executeQuery("select Os.ServiceId,Os.ServiceName,P.ServiceFee,P.CurrentStatus,S.ServiceAvailableDay from OurService Os inner join Project P on Os.ServiceId=P.ServiceId inner join ServiceAvailability S on P.ServiceId=S.ServiceId where Os.ServiceId  not in (10,12)");
            
        
             UserT user;
            
            while(resultSet.next())
         {
            user = new UserT(resultSet.getInt("ServiceId"),resultSet.getString("ServiceName"),resultSet.getString("ServiceFee"),resultSet.getString("CurrentStatus"),resultSet.getString("ServiceAvailableDay"));
            userList.add(user);
            

         }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
         return userList;
    
    
    
    }
      
