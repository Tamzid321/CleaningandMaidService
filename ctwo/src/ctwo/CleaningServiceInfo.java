    private void SearchServiceNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchServiceNameKeyReleased
         try {
           
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=checkpointnotwo;selectMethod=cursor", "sa", "123456");
            
            String sql ="select Os.ServiceId,Os.ServiceName,P.ServiceFee,P.CurrentStatus,S.ServiceAvailableDay from OurService Os inner join Project P on Os.ServiceId=P.ServiceId inner join ServiceAvailability S on P.ServiceId=S.ServiceId and Os.ServiceId  not in (10,12) where ServiceName like ?";
            PreparedStatement pst = connection.prepareStatement(sql);
            pst.setString(1, SearchServiceName.getText()+"%");
            ResultSet rs =pst.executeQuery();
            if(rs.next())
            {
              String setServiceId = rs.getString("ServiceId");
             ServiceId.setText(setServiceId);
              String setServiceName = rs.getString("ServiceName");
              ServiceName.setText(setServiceName);
             String setServiceFee= rs.getString("ServiceFee");
             ServiceFee.setText(setServiceFee);
             String setCurrentStatus= rs.getString("CurrentStatus");
             CurrentStatus.setText(setCurrentStatus);
             String setServiceAvailableDay= rs.getString("ServiceAvailableDay");
             ServiceAvailableDay.setText(setServiceAvailableDay);
             
              
            
            }
         }
         catch(Exception e)
         {
             JOptionPane.showMessageDialog(null, e);
         
         }
    }//GEN-LAST:event_SearchServiceNameKeyReleased

